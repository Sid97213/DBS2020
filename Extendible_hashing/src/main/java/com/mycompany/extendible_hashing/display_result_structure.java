    public String convertDecimalToBinary(int N, int n) {

    StringBuilder binary = new StringBuilder(32);

    while (N > 0 ) {
        binary.append( N % 2 );
        N >>= 1;
     }
    while(binary.length()!= n){
        binary.append(0);
    }

    return binary.reverse().toString();

    }
     
